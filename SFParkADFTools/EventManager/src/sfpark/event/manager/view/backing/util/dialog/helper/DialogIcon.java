package sfpark.event.manager.view.backing.util.dialog.helper;

public enum DialogIcon {

    /**
     * Asterisk icon (just an '*')
     */
    required,

    /**
     * Warning icon (triangle with '!')
     */
    warning,

    /**
     * Information icon (circle with 'i')
     */
    info,

    /**
     * Error icon (circle with 'x')
     */
    error,

    /**
     * Oracle icon (black letters 'ORACLE')
     */
    logo,

    /**
     * Changed icon (heavy blue dot)
     */
    changed;

}
