package sfpark.adf.tools.model.helper.dO;

import sfpark.adf.tools.model.data.dO.rateAreas.RateAreasDO;

public class RateAreasDOStatus implements DOStatusInterface {

    private boolean exists;
    private RateAreasDO DO;

    public RateAreasDOStatus(RateAreasDO DO) {
        super();

        this.exists = (DO != null);
        this.DO = DO;
    }

    public boolean existsDO() {
        return exists;
    }

    public RateAreasDO getDO() {
        return DO;
    }
}
