package sfpark.adf.tools.model.helper.dO;

import sfpark.adf.tools.model.data.dO.blocks.BlocksDO;

public class BlocksDOStatus implements DOStatusInterface {

    private boolean exists;
    private BlocksDO DO;

    public BlocksDOStatus(BlocksDO DO) {
        super();

        this.exists = (DO != null);
        this.DO = DO;
    }

    public boolean existsDO() {
        return exists;
    }

    public BlocksDO getDO() {
        return DO;
    }
}
