package sfpark.adf.tools.model.helper.dO;

import sfpark.adf.tools.model.data.dO.parkingSpaceGroups.ParkingSpaceGroupsDO;

public class ParkingSpaceGroupsDOStatus implements DOStatusInterface {

    private boolean exists;
    private ParkingSpaceGroupsDO DO;

    public ParkingSpaceGroupsDOStatus(ParkingSpaceGroupsDO DO) {
        super();

        this.exists = (DO != null);
        this.DO = DO;
    }

    public boolean existsDO() {
        return exists;
    }

    public ParkingSpaceGroupsDO getDO() {
        return DO;
    }
}
