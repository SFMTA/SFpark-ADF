package sfpark.adf.tools.model.helper.dto;

import sfpark.adf.tools.model.data.dto.rateChange.RateChangeHeaderDTO;

public class RateChangeHeaderDTOStatus implements DTOStatusInterface {

    private boolean exists;
    private RateChangeHeaderDTO DTO;

    public RateChangeHeaderDTOStatus(RateChangeHeaderDTO DTO) {
        super();

        this.exists = (DTO != null);
        this.DTO = DTO;
    }

    public boolean existsDTO() {
        return exists;
    }

    public RateChangeHeaderDTO getDTO() {
        return DTO;
    }
}
